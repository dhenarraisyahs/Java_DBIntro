    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(jdcTL.getDate());
        
        if ("".equals(jtfNis.getText()) || "".equals(jtaAlamat.getText()) || "".equals(jtfKelas.getText()) || tanggal.equals("") || "".equals(jtfNama.getText()) || "".equals(jtfEmail.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String JK = "";
            if (jrbLK.isSelected()) {
                JK = "L";
            } else {
                JK = "P";
            }
            String SQL = "INSERT INTO t_siswa (NIS,NamaSiswa,TanggalLahir,JenisKelamin,Kelas,Email,Alamat) "
                    + "VALUES('" + jtfNis.getText() + "','" + jtfNama.getText() + "','" + tanggal + "','" + JK + "','"
                    + jtfKelas.getText() + "','" +jtfEmail.getText() + "','" + jtaAlamat.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbSaveActionPerformed

